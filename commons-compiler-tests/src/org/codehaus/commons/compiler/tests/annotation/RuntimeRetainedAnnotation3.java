package org.codehaus.commons.compiler.tests.annotation;

import static java.lang.annotation.ElementType.CONSTRUCTOR;
import static java.lang.annotation.ElementType.FIELD;
import static java.lang.annotation.ElementType.LOCAL_VARIABLE;
import static java.lang.annotation.ElementType.METHOD;
import static java.lang.annotation.ElementType.PARAMETER;
import static java.lang.annotation.ElementType.TYPE;
import static java.lang.annotation.RetentionPolicy.RUNTIME;

import java.lang.annotation.Retention;
import java.lang.annotation.Target;

/**
 * An annotation type with retention policy "Runtime".
 */
@Target({ CONSTRUCTOR, FIELD, LOCAL_VARIABLE, METHOD, PARAMETER, TYPE })
@Retention(RUNTIME)
public
@interface RuntimeRetainedAnnotation3 {
    boolean  booleanValue();
    byte     byteValue();
    short    shortValue();
    int      intValue();
    long     longValue();
    float    floatValue();
    double   doubleValue();
    char     charValue();
    String   stringValue();
    Class<?> classValue();
    Override annotationValue();
    String[] stringArrayValue();
    int[]    intArrayValue();
}
